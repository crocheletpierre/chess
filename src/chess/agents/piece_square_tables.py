def lookup(piece_type: type, row: int, col: int, is_white: bool) -> int:
    """Return the positional bonus for a piece at (row, col).

    Tables are defined from White's perspective (row 7 = rank 1).
    For Black the table is flipped vertically.
    """
    table = TABLES[piece_type]
    effective_row = row if is_white else 7 - row
    return table[effective_row][col]